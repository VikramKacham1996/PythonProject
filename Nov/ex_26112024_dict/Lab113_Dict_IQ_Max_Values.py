#{"a":10,"b":20,"c":30,"d":40}
def max_value_dict(dictionary):
    return max(dictionary.values())

def min_value_dict(dictionary):
    return min(dictionary.values())

print(max_value_dict({"a":10,"b":20,"c":30,"d":40}))
print(min_value_dict({"a":10,"b":20,"c":30,"d":40}))
  



