
def print_mul_args(*args):
    for i  in args:
        print(i)


print_mul_args(1,2,3,4,5)
print_mul_args(1,2,3,4,5,6)
print_mul_args(1,2,3,4,5,6,7)
print("vik", "vikky")
print_mul_args("vik", "vikky")
print_mul_args("vik", "vikky", "vikky")
print_mul_args("vik", "2222vikky", "222vikky", "22vikky")



